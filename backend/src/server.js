const express = require('express');
const cors = require('cors');
require('dotenv').config();

const apiRoutes = require('./routes/api.routes');

const app = express();
const PORT = process.env.PORT || 3000;

// Middleware
app.use(cors());
app.use(express.json());
app.use(express.urlencoded({ extended: true }));

// Ğ›Ğ¾Ğ³ÑƒĞ²Ğ°Ğ½Ğ½Ñ Ğ·Ğ°Ğ¿Ğ¸Ñ‚Ñ–Ğ²
app.use((req, res, next) => {
  console.log(`${new Date().toISOString()} - ${req.method} ${req.path}`);
  next();
});

// API Routes
app.use('/api', apiRoutes);

// Ğ“Ğ¾Ğ»Ğ¾Ğ²Ğ½Ğ° ÑÑ‚Ğ¾Ñ€Ñ–Ğ½ĞºĞ° API
app.get('/', (req, res) => {
  res.json({
    message: 'EV Route Planner API',
    version: '1.0.0',
    endpoints: {
      buildRoute: 'POST /api/route/build',
      chargingStations: 'GET /api/charging-stations',
      health: 'GET /api/health'
    }
  });
});

// Health check
app.get('/api/health', (req, res) => {
  res.json({ status: 'OK', timestamp: new Date().toISOString() });
});

// ĞĞ±Ñ€Ğ¾Ğ±ĞºĞ° Ğ¿Ğ¾Ğ¼Ğ¸Ğ»Ğ¾Ğº 404
app.use((req, res) => {
  res.status(404).json({ error: 'Endpoint not found' });
});

// Ğ“Ğ»Ğ¾Ğ±Ğ°Ğ»ÑŒĞ½Ğ° Ğ¾Ğ±Ñ€Ğ¾Ğ±ĞºĞ° Ğ¿Ğ¾Ğ¼Ğ¸Ğ»Ğ¾Ğº
app.use((err, req, res, next) => {
  console.error('Error:', err);
  res.status(err.status || 500).json({
    error: err.message || 'Internal server error',
    ...(process.env.NODE_ENV === 'development' && { stack: err.stack })
  });
});

// Ğ—Ğ°Ğ¿ÑƒÑĞº ÑĞµÑ€Ğ²ĞµÑ€Ğ°
if (process.env.NODE_ENV !== 'test') {
  app.listen(PORT, () => {
    console.log(`ğŸš— EV Route Planner API running on port ${PORT}`);
    console.log(`ğŸ“ Environment: ${process.env.NODE_ENV || 'development'}`);
  });
}

module.exports = app;